#slicing /selecting subsequences
lang = "python"
#syntax - [start arguement: stop arguement:step]
print("Pradeep"[2:5])     #output= ade
print("Pradeep"[2:5:1])   #output= ade
print("Pradeep"[2:5:2])   #output= ae 
print("Pradeep"[0::2])    #output= Paep
print("Pradeep"[8::-1])   #output = peedarP
print("Pradeep"[::-1])    #output = peedarP (TRICK)
